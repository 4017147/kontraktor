package org.nustaq.kontraktor.annotations;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

@Retention(RetentionPolicy.RUNTIME)
@Target({ElementType.TYPE})

/**
 *
 * class modifier (actor). If a remoted facade actor is tagged with @Secured, only
 * methods marked with @Remoted can be called from a remote client.
 * Prevents attacks based on reflection
 *
 * Created by ruedi on 18.10.14.
 */
public @interface Secured {
}
